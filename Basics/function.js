// Parametered Function
function addition(n1, n2) {
  console.log("Addition of 2 numbers is= ", n1 + n2);
}
addition(5, 7); // You must declare all parameter's Values.

// Return Function
function additionF(n3, n4) {
  var sum = n3 + n4;
  return sum;
}
var summation = additionF(55, 70);
console.log("Addition of 2 numbers is= ", summation);

