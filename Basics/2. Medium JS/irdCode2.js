function serialNumbers(numbers) {
  var serial = [];
  for (let i = 0; i < numbers.length; i++) {
    var inc = numbers[i + 1] - numbers[i];
    var startSrial = 0;

    for (let j = 0; j < inc; j++) {
      if (startSrial <= inc) {
        startSrial++;
        serial.push(startSrial);
      }
    }
  }
  return serial;
}
